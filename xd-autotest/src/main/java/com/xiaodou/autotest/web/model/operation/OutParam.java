package com.xiaodou.autotest.web.model.operation;

public class OutParam {
	public OutParam() {
	}

	/** name */
	private String name;
	/** desc 赋值语句 */
	private String desc;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

}
